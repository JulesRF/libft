#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c <= 'z' && c >= 'a') || (c <= 'Z' && c >= 'A'))
		return (8);
	if (c <= '9' && c >= '0')
		return (8);
	return (0);
}
